-- 11) alarm
CREATE TABLE alarm (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sera_id         BIGINT        NOT NULL,
  alarm_turu      VARCHAR(40)   NOT NULL,
  mesaj           VARCHAR(200),
  deger           NUMERIC(8,2),                -- alarmi tetikleyen olcum degeri (orn. pH/EC/su)
  olusma_zamani   TIMESTAMPTZ   NOT NULL DEFAULT now(),
  cozulme_zamani  TIMESTAMPTZ,
  durum           alarm_durum_t NOT NULL DEFAULT 'AKTIF',
  CONSTRAINT fk_alarm_sera FOREIGN KEY (sera_id) REFERENCES sera(id)
    ON UPDATE CASCADE ON DELETE CASCADE,
  CONSTRAINT chk_alarm_cozulme CHECK (cozulme_zamani IS NULL OR cozulme_zamani >= olusma_zamani)
);
CREATE INDEX idx_alarm_sera_durum ON alarm (sera_id, durum);

-- ---- Tetikleyici: alarm kapanış zamanı -----------------------------------
CREATE OR REPLACE FUNCTION trg_alarm_kapanis() RETURNS trigger AS $$
BEGIN
  IF NEW.durum = 'COZULDU' AND OLD.durum <> 'COZULDU' AND NEW.cozulme_zamani IS NULL THEN
    NEW.cozulme_zamani := now();
  ELSIF NEW.durum = 'AKTIF' THEN
    NEW.cozulme_zamani := NULL;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;
CREATE TRIGGER trg_alarm_kapanis_b BEFORE UPDATE ON alarm
  FOR EACH ROW EXECUTE FUNCTION trg_alarm_kapanis();

-- ---- Tetikleyici: dozlama sonrası tank stok düşümü -----------------------
CREATE OR REPLACE FUNCTION trg_dozlama_stok() RETURNS trigger AS $$
BEGIN
  UPDATE tank
     SET mevcut_litre = GREATEST(0, mevcut_litre - NEW.miktar_ml / 1000.0)
   WHERE id = NEW.tank_id;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;
CREATE TRIGGER trg_dozlama_stok_a AFTER INSERT ON dozlama_kaydi
  FOR EACH ROW EXECUTE FUNCTION trg_dozlama_stok();

-- ============================= GÖRÜNÜMLER =================================
CREATE VIEW v_son_okuma AS
  SELECT sera_id, olcum_zamani, ph, ec, sicaklik, su_seviyesi
    FROM (
      SELECT s.*, ROW_NUMBER() OVER (
               PARTITION BY sera_id ORDER BY olcum_zamani DESC, id DESC) AS sira
        FROM sensor_okuma s
    ) t WHERE sira = 1;

CREATE VIEW v_aktif_alarm AS
  SELECT * FROM alarm WHERE durum = 'AKTIF';

CREATE VIEW v_gunluk_dozlama AS
  SELECT d.sera_id, t.cozelti_turu, d.dozlama_zamani::date AS gun,
         COUNT(*) AS dozlama_sayisi, SUM(d.miktar_ml) AS toplam_ml
    FROM dozlama_kaydi d
    JOIN tank t ON t.id = d.tank_id
   GROUP BY d.sera_id, t.cozelti_turu, d.dozlama_zamani::date;

