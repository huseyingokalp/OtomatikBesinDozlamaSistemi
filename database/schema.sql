  CONSTRAINT uq_evre_recete_sira UNIQUE (recete_id, sira_no),
  CONSTRAINT chk_evre_ph CHECK (hedef_ph_min >= 0 AND hedef_ph_max <= 14 AND hedef_ph_min < hedef_ph_max),
  CONSTRAINT chk_evre_ec CHECK (hedef_ec_min >= 0 AND hedef_ec_min < hedef_ec_max)
