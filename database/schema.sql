-- 4) buyume_evresi
CREATE TABLE buyume_evresi (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  evre_kodu     VARCHAR(30)  NOT NULL UNIQUE,
  recete_id     BIGINT       NOT NULL,
  sira_no       SMALLINT     NOT NULL,
  evre_ad       VARCHAR(80)  NOT NULL,
