-- Sensör kalibrasyon geçmişi (ofset/eğim düzeltmeleri)
CREATE TABLE kalibrasyon (
  id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cihaz_id           BIGINT      NOT NULL,
  parametre          VARCHAR(20) NOT NULL,            -- 'pH','EC','sicaklik'
  ofset              NUMERIC(8,4) NOT NULL DEFAULT 0,
  egim               NUMERIC(8,4) NOT NULL DEFAULT 1,
  kalibrasyon_zamani TIMESTAMPTZ NOT NULL DEFAULT now(),
  aciklama           TEXT,
  CONSTRAINT fk_kalib_cihaz FOREIGN KEY (cihaz_id) REFERENCES cihaz(id)
    ON UPDATE CASCADE ON DELETE CASCADE
);

-- Sensör/aktüatör arıza ve işaret kayıtları (kestirimci bakım)
CREATE TABLE sensor_ariza (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cihaz_id    BIGINT      NOT NULL,
  ariza_turu  VARCHAR(40) NOT NULL,                  -- 'kayma','takili_deger','aralik_disi','iletisim'
  baslangic   TIMESTAMPTZ NOT NULL DEFAULT now(),
  bitis       TIMESTAMPTZ,
  detay       TEXT,
  CONSTRAINT fk_ariza_cihaz FOREIGN KEY (cihaz_id) REFERENCES cihaz(id)
    ON UPDATE CASCADE ON DELETE CASCADE
);

-- Model kayıt defteri (sürüm + başarım metrikleri + konum)
CREATE TABLE model_kayit (
  id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ad               VARCHAR(80) NOT NULL,             -- 'hub_tinyml_anomali','sunucu_ph_tahmin'...
  surum            VARCHAR(40) NOT NULL,
  konum            VARCHAR(20) NOT NULL DEFAULT 'sunucu',  -- 'hub'|'sunucu'|'bulut'
  metrik_json      JSONB,
  egitim_baslangic TIMESTAMPTZ,
  egitim_bitis     TIMESTAMPTZ,
  olusturma_zamani TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT uq_model UNIQUE (ad, surum),
  CONSTRAINT chk_model_konum CHECK (konum IN ('hub','sunucu','bulut'))
);

-- Tahmin logu (tahmin vs gerçek -> drift izleme)
CREATE TABLE tahmin_log (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  model_id      BIGINT      NOT NULL,
  sera_id       BIGINT,
  tahmin_zamani TIMESTAMPTZ NOT NULL DEFAULT now(),
  parametre     VARCHAR(20),
  tahmin        NUMERIC(10,4),
  gercek        NUMERIC(10,4),
  CONSTRAINT fk_tahmin_model FOREIGN KEY (model_id) REFERENCES model_kayit(id)
    ON UPDATE CASCADE ON DELETE CASCADE,
  CONSTRAINT fk_tahmin_sera FOREIGN KEY (sera_id) REFERENCES sera(id)
    ON UPDATE CASCADE ON DELETE SET NULL
);

-- Karar logu (gölge mod önerileri; aktüatöre uygulanıp uygulanmadığı)
CREATE TABLE karar_log (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  model_id     BIGINT,
  sera_id      BIGINT,
  karar_zamani TIMESTAMPTZ NOT NULL DEFAULT now(),
  oneri        TEXT,
  uygulandi    BOOLEAN     NOT NULL DEFAULT false,
  CONSTRAINT fk_karar_model FOREIGN KEY (model_id) REFERENCES model_kayit(id)
    ON UPDATE CASCADE ON DELETE SET NULL,
  CONSTRAINT fk_karar_sera FOREIGN KEY (sera_id) REFERENCES sera(id)
    ON UPDATE CASCADE ON DELETE SET NULL
);

-- Çevrimdışı RL geçişleri: (durum, eylem, ödül, sonraki durum)
CREATE TABLE gecis (
  id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sera_id            BIGINT,
  zaman              TIMESTAMPTZ NOT NULL DEFAULT now(),
  durum_json         JSONB       NOT NULL,
  eylem_json         JSONB       NOT NULL,
  odul               NUMERIC(12,5),
  sonraki_durum_json JSONB,
  CONSTRAINT fk_gecis_sera FOREIGN KEY (sera_id) REFERENCES sera(id)
    ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE INDEX idx_okuma_kalite ON sensor_okuma (kalite_bayragi, olcum_zamani DESC);
