  FOR EACH ROW EXECUTE FUNCTION trg_set_guncellenme();

-- ===================== ZAMAN SERİSİ / OLAY TABLOLARI ======================

-- 8) sensor_okuma (yüksek hacimli zaman serisi)
CREATE TABLE sensor_okuma (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sera_id       BIGINT      NOT NULL,
  olcum_zamani  TIMESTAMPTZ NOT NULL DEFAULT now(),
  ph            NUMERIC(4,2),
  ec            NUMERIC(5,2),
  sicaklik      NUMERIC(4,1),
  su_seviyesi   NUMERIC(5,2),
  kalite_bayragi SMALLINT  NOT NULL DEFAULT 1,   -- 1=normal, 2=supheli, 3=karantina (yardimci YZ/TinyML)
  kaynak         VARCHAR(12) NOT NULL DEFAULT 'olay',  -- 'olay' (ham, olay tabanli) | 'ozet' (pencere ozetinden) | 'elle'
  kaynak        VARCHAR(40),                      -- veri kaynagi / hub kimligi (idempotentlik/izlenebilirlik)
  CONSTRAINT fk_okuma_sera FOREIGN KEY (sera_id) REFERENCES sera(id)
    ON UPDATE CASCADE ON DELETE CASCADE,
  CONSTRAINT chk_okuma_ph CHECK (ph IS NULL OR (ph >= 0 AND ph <= 14)),
  CONSTRAINT chk_okuma_ec CHECK (ec IS NULL OR ec >= 0),
  CONSTRAINT chk_okuma_su CHECK (su_seviyesi IS NULL OR (su_seviyesi >= 0 AND su_seviyesi <= 100)),
  CONSTRAINT chk_okuma_kalite CHECK (kalite_bayragi IN (1,2,3)),
  CONSTRAINT chk_okuma_kaynak CHECK (kaynak IN ('olay','ozet','elle'))
);
CREATE INDEX idx_okuma_sera_zaman ON sensor_okuma (sera_id, olcum_zamani DESC);
-- Ekleme-ağırlıklı zaman sütunu için alan-verimli BRIN indeksi
CREATE INDEX idx_okuma_zaman_brin ON sensor_okuma USING BRIN (olcum_zamani);

-- 9) sensor_ozet (pencere özetleri / downsampling)
CREATE TABLE sensor_ozet (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sera_id       BIGINT      NOT NULL,
  pencere_basi  TIMESTAMPTZ NOT NULL,
  ornek_sayisi  INTEGER     NOT NULL,
  ph_ort NUMERIC(4,2), ph_min NUMERIC(4,2), ph_max NUMERIC(4,2),
  ec_ort NUMERIC(5,2), ec_min NUMERIC(5,2), ec_max NUMERIC(5,2),
  CONSTRAINT fk_ozet_sera FOREIGN KEY (sera_id) REFERENCES sera(id)
    ON UPDATE CASCADE ON DELETE CASCADE,
  CONSTRAINT uq_ozet_sera_pencere UNIQUE (sera_id, pencere_basi),
  CONSTRAINT chk_ozet_ornek CHECK (ornek_sayisi > 0)
);
CREATE INDEX idx_ozet_sera_zaman ON sensor_ozet (sera_id, pencere_basi DESC);

-- 10) dozlama_kaydi
CREATE TABLE dozlama_kaydi (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sera_id         BIGINT       NOT NULL,
  tank_id         BIGINT       NOT NULL,
  cihaz_id        BIGINT,
  parti_id        BIGINT,
