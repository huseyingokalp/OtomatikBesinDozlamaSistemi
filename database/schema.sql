\connect cilek_dozlama

-- ---- ENUM etki alanları --------------------------------------------------
CREATE TYPE durum_t        AS ENUM ('AKTIF','PASIF');
CREATE TYPE cozelti_turu_t AS ENUM ('ASIT','BAZ','BESIN_A','BESIN_B','SU');
CREATE TYPE cihaz_turu_t   AS ENUM ('POMPA','VALF','SENSOR');
CREATE TYPE tetik_turu_t   AS ENUM ('OTOMATIK','MANUEL');
CREATE TYPE parti_durum_t  AS ENUM ('AKTIF','HASAT_EDILDI','IPTAL');
CREATE TYPE alarm_durum_t  AS ENUM ('AKTIF','COZULDU');

-- ---- Ortak: guncellenme + surum (iyimser kilitleme) tetikleyicisi --------
CREATE OR REPLACE FUNCTION trg_set_guncellenme() RETURNS trigger AS $$
BEGIN
  NEW.guncellenme := now();
  NEW.surum := OLD.surum + 1;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- ============================ ANA TABLOLAR =================================

-- 1) kullanici
CREATE TABLE kullanici (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  kullanici_adi   VARCHAR(50)  NOT NULL UNIQUE,
  parola_hash     CHAR(64)     NOT NULL,
  ad_soyad        VARCHAR(100) NOT NULL,
  yetki_seviyesi  SMALLINT     NOT NULL DEFAULT 0,
  olusturulma     TIMESTAMPTZ  NOT NULL DEFAULT now(),
  guncellenme     TIMESTAMPTZ  NOT NULL DEFAULT now(),
  surum           INTEGER      NOT NULL DEFAULT 1,
  CONSTRAINT chk_kullanici_yetki CHECK (yetki_seviyesi IN (0,1)),
  CONSTRAINT chk_kullanici_ad    CHECK (char_length(kullanici_adi) >= 3)
);
CREATE TRIGGER trg_kullanici_upd BEFORE UPDATE ON kullanici
  FOR EACH ROW EXECUTE FUNCTION trg_set_guncellenme();

-- 2) sera
CREATE TABLE sera (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sera_kodu    VARCHAR(30)  NOT NULL UNIQUE,
  sera_ad      VARCHAR(100) NOT NULL,
  konum        VARCHAR(150),
  olusturulma  TIMESTAMPTZ  NOT NULL DEFAULT now(),
  guncellenme  TIMESTAMPTZ  NOT NULL DEFAULT now(),
  surum        INTEGER      NOT NULL DEFAULT 1
);
CREATE TRIGGER trg_sera_upd BEFORE UPDATE ON sera
  FOR EACH ROW EXECUTE FUNCTION trg_set_guncellenme();

-- 3) recete
CREATE TABLE recete (
