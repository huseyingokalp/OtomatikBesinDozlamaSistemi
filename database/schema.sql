  degisim         NUMERIC(6,2) GENERATED ALWAYS AS (sonrasi - oncesi) STORED,
  tetik_turu      tetik_turu_t NOT NULL DEFAULT 'OTOMATIK',
  CONSTRAINT fk_doz_sera  FOREIGN KEY (sera_id)  REFERENCES sera(id)  ON UPDATE CASCADE ON DELETE CASCADE,
  CONSTRAINT fk_doz_tank  FOREIGN KEY (tank_id)  REFERENCES tank(id)  ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT fk_doz_cihaz FOREIGN KEY (cihaz_id) REFERENCES cihaz(id) ON UPDATE CASCADE ON DELETE SET NULL,
  CONSTRAINT fk_doz_parti FOREIGN KEY (parti_id) REFERENCES parti(id) ON UPDATE CASCADE ON DELETE SET NULL,
  CONSTRAINT chk_doz_miktar CHECK (miktar_ml > 0)
